// Generate a rich canvas texture for each artwork
export function makeArtworkTexture(art) {
  const W = 400, H = 520;
  const cv = document.createElement('canvas');
  cv.width = W; cv.height = H;
  const ctx = cv.getContext('2d');
  const p = art.palette;

  // Background gradient
  const bg = ctx.createLinearGradient(0, 0, W, H);
  bg.addColorStop(0, p[0]);
  bg.addColorStop(0.5, p[1]);
  bg.addColorStop(1, p[2] || p[0]);
  ctx.fillStyle = bg;
  ctx.fillRect(0, 0, W, H);

  // Style-specific overlays
  if (art.style === 'landscape' || art.style === 'dawn') {
    // Horizon line
    const hg = ctx.createLinearGradient(0, H*0.5, 0, H);
    hg.addColorStop(0, p[3] + '88');
    hg.addColorStop(1, p[2] + 'cc');
    ctx.fillStyle = hg;
    ctx.fillRect(0, H*0.5, W, H*0.5);
    // Sun/glow
    const rg = ctx.createRadialGradient(W*0.6, H*0.45, 0, W*0.6, H*0.45, W*0.4);
    rg.addColorStop(0, (p[3] || p[1]) + 'ff');
    rg.addColorStop(1, 'transparent');
    ctx.fillStyle = rg;
    ctx.fillRect(0, 0, W, H);
  }

  if (art.style === 'cityscape' || art.style === 'night') {
    // Bokeh lights
    for (let i = 0; i < 80; i++) {
      const bx = Math.random()*W, by = Math.random()*H*0.7;
      const br = 1 + Math.random()*4;
      const rg = ctx.createRadialGradient(bx, by, 0, bx, by, br*3);
      rg.addColorStop(0, p[i%p.length] + 'ff');
      rg.addColorStop(1, 'transparent');
      ctx.fillStyle = rg;
      ctx.beginPath(); ctx.arc(bx, by, br*3, 0, Math.PI*2); ctx.fill();
    }
  }

  if (art.style === 'misty' || art.style === 'storm') {
    // Layered mist bands
    for (let i = 0; i < 6; i++) {
      const my = H*0.1 + i*H*0.12;
      const mg = ctx.createLinearGradient(0, my, 0, my+H*0.12);
      mg.addColorStop(0, p[2] + '00');
      mg.addColorStop(0.5, p[3] + '55');
      mg.addColorStop(1, p[2] + '00');
      ctx.fillStyle = mg;
      ctx.fillRect(0, my, W, H*0.12);
    }
  }

  if (art.style === 'underwater') {
    // Light rays
    for (let i = 0; i < 8; i++) {
      const rx = W*0.1 + i*W*0.1;
      const grad = ctx.createLinearGradient(rx, 0, rx+30, H);
      grad.addColorStop(0, p[2] + '44');
      grad.addColorStop(1, 'transparent');
      ctx.fillStyle = grad;
      ctx.beginPath();
      ctx.moveTo(rx, 0); ctx.lineTo(rx+60, H); ctx.lineTo(rx+30, H); ctx.lineTo(rx-30, 0);
      ctx.fill();
    }
  }

  if (art.style === 'icm') {
    // Vertical streaks
    for (let i = 0; i < 40; i++) {
      const sx = Math.random()*W;
      const grad = ctx.createLinearGradient(sx, 0, sx+5, H);
      grad.addColorStop(0, p[i%p.length] + '88');
      grad.addColorStop(0.5, p[(i+1)%p.length] + 'cc');
      grad.addColorStop(1, p[i%p.length] + '44');
      ctx.strokeStyle = grad;
      ctx.lineWidth = 1.5 + Math.random()*4;
      ctx.beginPath(); ctx.moveTo(sx, 0); ctx.lineTo(sx+Math.random()*20-10, H); ctx.stroke();
    }
  }

  if (art.style === 'festival') {
    // Floating circles (lanterns)
    for (let i = 0; i < 60; i++) {
      const lx = Math.random()*W, ly = Math.random()*H;
      const lr = 3 + Math.random()*12;
      const rg = ctx.createRadialGradient(lx, ly, 0, lx, ly, lr*2);
      rg.addColorStop(0, p[3] + 'ff');
      rg.addColorStop(0.5, p[2] + '88');
      rg.addColorStop(1, 'transparent');
      ctx.fillStyle = rg;
      ctx.beginPath(); ctx.arc(lx, ly, lr*2, 0, Math.PI*2); ctx.fill();
    }
  }

  if (art.style === 'geometric') {
    // Grid of light shapes
    for (let row = 0; row < 6; row++) {
      for (let col = 0; col < 5; col++) {
        if (Math.random() > 0.4) {
          ctx.fillStyle = p[3] + Math.floor(Math.random()*150+50).toString(16);
          const sx = col*W/4, sy = row*H/5;
          ctx.fillRect(sx+5, sy+5, W/4-10, H/5-10);
        }
      }
    }
  }

  // Organic texture overlay (noise-like)
  for (let i = 0; i < 200; i++) {
    const tx = Math.random()*W, ty = Math.random()*H;
    const tr = 20+Math.random()*60;
    const ta = Math.random()*0.06;
    ctx.fillStyle = `rgba(255,255,255,${ta})`;
    ctx.beginPath(); ctx.arc(tx, ty, tr, 0, Math.PI*2); ctx.fill();
  }

  // Vignette
  const vg = ctx.createRadialGradient(W/2, H/2, W*0.2, W/2, H/2, W*0.85);
  vg.addColorStop(0, 'transparent');
  vg.addColorStop(1, 'rgba(0,0,0,0.45)');
  ctx.fillStyle = vg;
  ctx.fillRect(0, 0, W, H);

  // Text watermark
  ctx.save();
  ctx.globalAlpha = 0.18;
  ctx.fillStyle = '#fff';
  ctx.font = `italic 500 ${W*0.065}px 'Cormorant Garamond', serif`;
  ctx.textAlign = 'center';
  ctx.fillText(art.title, W/2, H/2 + W*0.03);
  ctx.font = `300 ${W*0.032}px 'Inter', sans-serif`;
  ctx.fillText(art.artist, W/2, H/2 + W*0.085);
  ctx.restore();

  return new THREE.CanvasTexture(cv);
}

// Draw a high-res version into an existing canvas (for the info panel)
export function drawInfoThumb(art, canvas) {
  const W = canvas.width, H = canvas.height;
  const ctx = canvas.getContext('2d');
  const p = art.palette;

  const bg = ctx.createLinearGradient(0, 0, W, H);
  bg.addColorStop(0, p[0]);
  bg.addColorStop(1, p[1]);
  ctx.fillStyle = bg;
  ctx.fillRect(0, 0, W, H);

  const vg = ctx.createRadialGradient(W/2, H/2, 10, W/2, H/2, W*0.8);
  vg.addColorStop(0, (p[2]||p[0]) + '55');
  vg.addColorStop(1, 'rgba(0,0,0,0.5)');
  ctx.fillStyle = vg;
  ctx.fillRect(0, 0, W, H);

  ctx.save();
  ctx.globalAlpha = 0.3;
  ctx.fillStyle = '#fff';
  ctx.font = `italic ${W*0.12}px 'Cormorant Garamond', serif`;
  ctx.textAlign = 'center';
  ctx.textBaseline = 'middle';
  ctx.fillText(art.title, W/2, H/2);
  ctx.restore();
}